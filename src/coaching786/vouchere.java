    private void DOWNLOADKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_DOWNLOADKeyReleased


if(evt.getKeyCode()==KeyEvent.VK_ENTER){        PrinterJob job = PrinterJob.getPrinterJob();
        job.setJobName("Print Data");

        job.setPrintable(new Printable(){
            public int print(Graphics pg,PageFormat pf, int pageNum){
                pf.setOrientation(PageFormat.LANDSCAPE);
                if(pageNum > 0){
                    return Printable.NO_SUCH_PAGE;
                }

                Graphics2D g2 = (Graphics2D)pg;
                g2.translate(pf.getImageableX(), pf.getImageableY());
                g2.scale(0.47,0.47);

                panelp11.print(g2);

                return Printable.PAGE_EXISTS;

            }
        });
        boolean ok = job.printDialog();
        if(ok){
            try{

                job.print();
            }
            catch (PrinterException ex){
                ex.printStackTrace();
            }
        }

     strecord r=new strecord();
r.setVisible(true);
dispose();}        // TODO add your handling code here:
    }//GEN-LAST:event_DOWNLOADKeyReleased

